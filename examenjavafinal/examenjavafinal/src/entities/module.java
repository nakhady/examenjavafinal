package entities;

public class module {
    private int id;
    private String nomModule;
    public module() {
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getNomModule() {
        return nomModule;
    }
    public void setNomModule(String nomModule) {
        this.nomModule = nomModule;
    }
    
}